    /**
     * Nos muestar un peque√±o indicador de la opcion del
     * menu.
     * @param newState 
     */
    private void changeMenuIndicator(short newState) {
        actualMenuId = newState;
        
        this.atenderPedidoIndicator.setVisible(false);
        this.verPedidoIndicator.setVisible(false);
        
        if(actualMenuId == 1) 
            atenderPedidoIndicator.setVisible(true);
        else if(actualMenuId == 2) {
            verPedidoIndicator.setVisible(true);
        }
        
    }
    
    /**
     * Mostramos determinado panel en elespacio de trabajo.
     * @param panel 
     */
    private void showInWorkspace(JPanel panel) {
        panel.setSize(workspace.getWidth(), workspace.getHeight());
        panel.setLocation(0, 0);
        
        workspace.removeAll();
        
        workspace.add(panel, BorderLayout.CENTER);
        workspace.revalidate();
        workspace.repaint();
    }

