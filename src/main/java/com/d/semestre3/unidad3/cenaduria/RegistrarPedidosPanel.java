    private boolean checkNumberInputsVal() {
        if(
                (Integer)(noEnchiladas.getValue()) <= 0 &&
                (Integer)(noTacos.getValue()) <= 0 &&
                (Integer)(noRefrescos.getValue()) <= 0 &&
                (Integer)(noPozole.getValue()) <= 0 &&
                (Integer)(noSopes.getValue()) <= 0
        ) {
            JOptionPane.showMessageDialog(null, "No hay ningun platillo agregado. Revise bien el pedido", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        return true;
    }

