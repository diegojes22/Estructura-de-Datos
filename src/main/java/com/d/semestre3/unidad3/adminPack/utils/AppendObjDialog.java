    /**
     * Boton de aceptar.
     * @param evt 
     */
    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        try {
            articulo.setNombre(nombreIn.getText());
            articulo.setCodigo(Integer.parseInt(codigoIn.getText()));
            articulo.setPrecio(Double.parseDouble(precioIn.getText()));
            articulo.setExistencia(Short.parseShort(stockIn.getText()));
            
            this.request.acceptIsPressed();
        } 
        catch(NumberFormatException e) {
            ErrorDialog.showDialog(parent, "Fallo", "<html><body>Revise los campos, ya que hay un dato incorrecto o invalido.</body></html>");
            return;
        }
        catch(Exception e) {
            ErrorDialog.showDialog(parent, "Fallo critico", "<html><body>"+e.getMessage()+"<br><br><b>"+e.getClass()+"</b></body></html>");
            this.request.windowClosedOrHiden();
        } 
        finally {
            this.setVisible(false);
        }
    }//GEN-LAST:event_okBtnActionPerformed

