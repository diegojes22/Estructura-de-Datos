    /**
     * Evento de "acpetar" para poder crear un objeto de tipo
     * Libro y guardarlo en el arbol.
     * 
     * @param evt 
     */
    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        if(!validateFields()) return;
        
        Libro libro = new Libro(
                this.tituloField.getText(),
                Integer.parseInt(this.isbnField.getText()),
                Integer.parseInt(this.paginasField.getText()),
                dayField.getSelectedItem()+"/"+monthField.getSelectedItem()+"/"+yearField.getSelectedItem(),
                this.editorialField.getText(),
                this.autorField.getText()
        );
        
        if(treebooks.add(libro)) {
            BasicDialogs.info(this, "El libro se agrego correctamente al arbol", "Listo");
            clearInputs();
        }
        else {
            BasicDialogs.warning(this, "No se pudo agregar el libro al arbol \nIntentelo de nuevo", "Advertencia");
        }
        
    }//GEN-LAST:event_aceptarActionPerformed

