    /**
     * Valida cada uno de los campos
     */
    @Override
    public boolean validateFields() {
        /// Validar fecha
        String day = (String)this.dayField.getSelectedItem();
        String month = (String)this.monthField.getSelectedItem();
        String year = (String)this.yearField.getSelectedItem();
        
        if(!DateUtils.validateDate(day, month, year)) {
            BasicDialogs.warning(this, "La fecha ingresada no es valida", "Fecha invalida");
            return false;
        }
        
        /// Validar Numero de paginas
        try {
            Integer.parseInt(this.paginasField.getText());
        } catch(Exception e) {
            BasicDialogs.warning(this, "No se ingreso un numero de paginas correcto", "Numero de paginas invalido");
            return false;
        }
        
        /// Validar ISBN
        try {
            Integer.parseInt(this.isbnField.getText());
        } catch(Exception e) {
            BasicDialogs.warning(this, "El ISBN no es valido, intente de nuevo por favor", "ISBN invalido");
            return false;
        }
        
        /// Validar el resto de campos
        if((!tituloField.getText().isBlank() && !editorialField.getText().isBlank() && !autorField.getText().isBlank()) == false) {
            BasicDialogs.warning(this, "Por favor, rellene los campos faltantes", "Campos faltantes");
            return false;
        }
        
        return true;
    }

